///////////////////////////////////////////////////////////////////////////////
//
// :: CONSTANTS
//
///////////////////////////////////////////////////////////////////////////////
const SET_PROXY_URL = 'SET_PROXY_URL';

///////////////////////////////////////////////////////////////////////////////
//
// :: ACTIONS
//
///////////////////////////////////////////////////////////////////////////////
export const setProxyUrl = (url) => {
  return {
    type: SET_PROXY_URL,
    payload: {
      url,
    },
  };
};

///////////////////////////////////////////////////////////////////////////////
//
// :: REDUCER
//
///////////////////////////////////////////////////////////////////////////////
const defaultState = null;

export const reducer = (state = defaultState, action) => {
  switch (action.type) {
    case SET_PROXY_URL:
      return action.payload.url;
    default:
      return state;
  }
};

///////////////////////////////////////////////////////////////////////////////
//
// :: SELECTORS
//
///////////////////////////////////////////////////////////////////////////////
export const selectProxyUrl = (store) => store.proxyUrl;
