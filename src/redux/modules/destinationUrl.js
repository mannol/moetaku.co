///////////////////////////////////////////////////////////////////////////////
//
// :: CONSTANTS
//
///////////////////////////////////////////////////////////////////////////////
const SET_DESTINATION_URL = 'SET_DESTINATION_URL';

///////////////////////////////////////////////////////////////////////////////
//
// :: ACTIONS
//
///////////////////////////////////////////////////////////////////////////////
export const setDestinationUrl = (url) => {
  return {
    type: SET_DESTINATION_URL,
    payload: {
      url,
    },
  };
};

///////////////////////////////////////////////////////////////////////////////
//
// :: REDUCER
//
///////////////////////////////////////////////////////////////////////////////
const defaultState = null;

export const reducer = (state = defaultState, action) => {
  switch (action.type) {
    case SET_DESTINATION_URL:
      return action.payload.url;
    default:
      return state;
  }
};

///////////////////////////////////////////////////////////////////////////////
//
// :: SELECTORS
//
///////////////////////////////////////////////////////////////////////////////
export const selectDestinationUrl = (store) => store.destinationUrl;
